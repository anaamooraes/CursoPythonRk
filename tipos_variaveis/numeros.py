#Inteiro
numero_inteiro = 3 
print("Inteiro = ", numero_inteiro)

#Real com ponto flutuante 
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

#Função type()
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))

#Soma +
soma = 1 + 1
print("1 + 1 =", soma )

#Subtração - 
subtracao = 1 - 1
print("1 - 1 =", subtracao)

#Multiplicação 
multiplicacao = 2 * 2
print("2 * 2 =", multiplicacao)

#Divisão / 
#Sempre que usarmos divisão, o resultado será float, para converter precisamos utilizar a função int 
divisao  = 5 / 2
print("5 / 2 =", divisao)
print("Tipo da variavel do resultado da divisão = ", type(divisao))

#Divisão inteiro / / 
divisao  = 5 // 2
print("5 / 2 =", divisao)
print("Tipo da variavel do resultado da divisão = ", type(divisao))

#int()
print("Valor em inteiro =", int(divisao))

#float()
print("Valor em float =", float(divisao))

#Modulo % 
modulo = 5 % 2 
print("Modulo =", modulo)
